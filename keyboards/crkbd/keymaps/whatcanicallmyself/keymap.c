static void render_layer_state_image(void) {
    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_raw_P(layer0,sizeof(layer0));
        case 1:
            oled_write_raw_P(my_logo,sizeof(my_logo));
        default:
            oled_write_raw_P(crkbd_logo,sizeof(crkbd_logo));
        }
}





bool oled_task_user(void) {
    if (is_keyboard_master()) {
        //oled_write_raw(read_layer_state_image(), sizeof(read_layer_state_image));
        render_layer_state_image();
    } else {
//         oled_write(read_logo(), false);
//         oled_write(read_keylog(), false);
    }
    return false;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
    set_keylog(keycode, record);
  }
  return true;
}
